public class Operadores {
    public float adicao(float c1, float c2) {
        return c1 + c2;
    }

    public float subtracao(float c1, float c2) {
        return c1 - c2;
    }

    public float multiplicacao(float c1, float c2) {
        return c1 * c2;
    }

    public float divisao(float c1, float c2) {
        return c1 / c2;
    }} 